        We are collecting new nationally representative household survey data among adults ages 65+ in Puerto Rico
        (n=1500) and Dominican Republic (n=1500) to estimate the prevalence of dementia and to capture the variation in
        lifecourse exposures to AD/ADRD risk factors, as well as family and societal effects of dementia.
