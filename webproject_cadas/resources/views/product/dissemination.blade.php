            Our research addresses an important issue and has implications for the public and policy. Generating new knowledge about the complex ways migration-related changes relate to socio-emotional development is critical because migration is so prevalent and because being a competent, well-adjusted social actor promotes societal wellbeing (Cohen, 2006). Our research can be used to educate families and impact public health. New knowledge about migration’s costs and benefits can help families as they adapt to the changes migration brings to relationships and caregiving. This research can also be used by interventionists because socialization is modifiable (Thomas & Zimmer-Gembeck, 2007). Our research has policy implications. It addresses the need for data on migration’s effects to inform children’s human rights protections (UNICEF Office of Research-Innocenti, 2019). Results may inform policy protecting against child labor that sometimes occurs after parents migrate, and policy regarding parent-child separation (Bouza et al., 2018; de la Garza, 2010).
